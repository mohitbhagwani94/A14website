						<h3> Details </h3>
						<div align="justify">
							<h3>Round 1: Natural Selection</h3>
  
A questionnaire consisting of 20 questions is given to the
participants, who turn up in teams of two, and will be tested based on their
capability for logical and analytical reasoning. Teams are selected on the
basis of cut off marks.
							

							<h3>Round 2: THE HUNT FOR THE JIGSAW</h3>
							
Jigsaw puzzles with names of various lab techniques
performed in the labs of the biotech block will be given to the participants.
The puzzle will consist of the name of the technique, and a brief description
of the same- both of which are revealed, as they follow clues listed out in a
sheet, each directing them to labs that house the pieces of the puzzle to piece
the jigsaw together.

							<h3>Round 3: THE TOOLS</h3>

Every trade has its tools and so does every technique. This
round lets the participant showcase his knowledge wherein he will have to
choose the appropriate instrumentation. Each questions answer will lead them to
the next instrument.  
	<h3>Round 4: FORENSICS INC.</h3>

Task 1 : CASE FILES
<br />
A case file is given to each team. Some time is allotted for
each team to sit together and read the case. Then, they are segregated- one
participant is asked to solve the case in the lab, using certain techniques
that we teach them; the other member is asked to solve the case by analyzing
evidences (say for example, footprints). The team members are allowed to sit
together to compile their findings and to arrive at a conclusion that justifies
their findings from the lab and the field.<br/><br/>

Task 2: Who Killed Pyarelal?
<br />
Participants are directed to a crime scene. They are asked
to collect evidences by interrogating live suspects and witnesses. They have to
figure out who the murderer is and bring justice to pyaarelal. 
