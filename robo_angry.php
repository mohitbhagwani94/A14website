Two chances are given per team. Points are earned on the basis of the distance covered and then added to a points tally. 

							

							<h3>Round 2</h3>
							The First Round comprises of the Robot projecting it at the target. There are a Total of 3 Targets with 
Different priorities at 1m, 3m and 6.5m respectively with a score multiplier of x1, x1.5 and x2 respectively. <br>
A total of 10 chances are given with a maximum time limit of 10 minutes.


							<h3>Round 3</h3>
							This round consists of a moving target on a plain surface with a score multiplier of x1. <br>
The target will move a variable distance for 2 seconds every time and stop for a period of 5 seconds. A time limit of 5 minutes is given. 
							
							<h3>Round 4</h3>
							This is the final round of the event which comprises of a one on one projectile fight.<br>
The teams are separated in their areas by a 35 cm high wall and have to destroy the other teams castle by shooting at it. Many obstacles are placed on the ground where the 
participants are shooting from and if the other team hits any of the obstacles, it is awarded some points.<br>
Time Limit – 8 Minutes


