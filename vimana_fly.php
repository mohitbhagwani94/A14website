							<h3>Round 1: PAPER PLANES</h3>
							In this round it is a combination of fun and full of creativity 
towards of engineering. This round challenges the participants to build a paper
plane from a A4 size sheet. A4 size sheets will be provided to teams at the
venue only. The team will be given around 10 minutes to construct the paper
plane. In this round there will be circular rings through which the paper plane
has to fly. Teams have to construct the plane from the given sheet and
they can modify it only by folding. No cutting, pasting and using of staplers

							

							<h3>Round 2: GLIDER MAKING FROM CARDBOARDSHEET</h3>
							The teams will be provided all the material required in this round by Aaruush. Participants
should build their own gliders only and fly them. Time provided to each team will be 1.5 hours.
Teams have to construct the glider at the venue only. To make it more exciting
and thrill constraints and obstacles are there in this round .Also the teams
has to answer the problem statement of this round.


							<h3>Round 3: GLIDER MAKING FROM HIGH DENSITY  POLYSTYRENE</h3>
							This round is higher version of Round 2 i.e. full of entertainment and fun.
