					Judges – MBA Faculty.

					<h3>Round 2</h3>
					In this round, the selected teams will be provided with the feedbacks given by the judges from the 1st round. 
					They also have to get feedback from as many potential customers as they can, to improve their B-Plan. Keeping all the feedbacks in their minds, they have to modify their B-Plans.
					Based on the modified B-Plan, 8-10 teams will be selected for the final round.

					<h3>Round 3</h3>
					In this round, the selected teams have to give a 20min detailed presentation of their B-Plan including all
					 the prototypes (flow charts, graphs, pictorial representation of their products(if any), etc.).
					Followed by the presentation, there will be a small 5 min Q & A session between the presenting team
