						<div align="justify">
						

<h3>Round 1: Chokola</h3>
A set of 3-5 word-clues will be given to the participants using which they fill up the crossword puzzle correctly. And then they have to
arrange the words into an interesting sentence involving chocolate.

<h3>Round 2: ChocoRush</h3>
A participant will have to guide a Cadbury Shot through a 3D path by blowing through a straw without touching the chocolate. For every time the chocolate touches the walls of the path, his other teammates will have to answer a question within 30 secs.
They can keep attempting  or give up within that time limit. For the first missed answer,they are allowed to proceed by adding 5 secs to the total time. For the second, 10 secs are added and so on. The top 30 teams to complete the task in the shortest duration qualify.

<h3>Round 3: Chocotrail </h3>
A calculator will be chalked out on the floor, comprising numbers 0-9 and various mathematical operations (+-x/=%âˆš etc). One member picks out a card bearing a number, which will be his starting point on the calculator (eg.12). The other 2 teammates have to make an equation for him with that number as the answer(eg. 4x3). Once he gets his equation, he has to hop on the corresponding tiles in the correct order (4,x,3,=). The twist here is that he is allowed a single-footed hop for 0 and odd numbers, and double-footed hop for
even numbers and symbols. The team has to make equations for 5 successive numbers without repeating the operators. The person who forgets/ misses a step gets eliminated.
                                                       
<h3>Round 4: ChocoSplash</h3>
Toffees will be present in a carton filled with thermocol balls. 3 teams will be competing at a time. First member fishes out a chocolate using chopsticks and carries it to a bowl next to the second member, who answers a riddle. Then the chocolate is passed on to the third member, who has to make a collage (identifiable image) using the various colourful wrappers.Time limit= 10 mins.
                                                                 
<h3>Round 5: ChocoStorm </h3>
Different shaped chocolates falling across the screen will be projected. Each team needs to keep count of the number of chocolates of each shape (eg. 5 square 3 triangle). The speed of
falling chocolates increases with time. The team giving the correct/closest answer gets a card corresponding to their sum of chocolates (in this case card no.8). Each card will bear the name of a chocolate. Using each alphabet they have to write a common scientific equation.
                                                               
<h3>Round 6: Chase The Chocolate</h3>                
Each team is provided with a set of 20 questions. One member
is blindfolded and has to go through a maze guided by his teammates. For each riddle solved by them correctly, he proceeds 3 steps forward, and 1 step backward for every wrong answer. At the end of the maze, a bowl of 5 assorted chocolates will be kept from which he needs to identify atleast 3 correctly just by tasting them (dark chocolate/milk chocolate/white chocolate etc). The
team to finish in the shortest time wins.



