<h3>Round 1: Crosshunt</h3>
Pictures will be shown to the participants. They need to identify, analyze and complete the crossword accordingly in a minute.

<h3>Round 2: Global Estimate</h3>
Participants need to analyze the number of facts he knows about
the category chosen at random by the coordinator. Ten facts
are then displayed and participants must answer them.

<h3>Round 3: Cancel the Bid       </h3>
Each participant is given a limited amount of virtual money. Participants need to bid for the
displayed product using the virtual money provided. Multiple biddings
are allowed for asingle product.

<h3>Round 4: Bidder’s Choice</h3>
List of the proposed virtual products will be displayed. An
open, live, outcry auction would be conducted for the players.
Players participate in the auction based upon the amount of virtual money
they are left with after round 2.

<h3>Round 5: Xchange currency</h3>
The products bought by the participant in round 3 and round 4 will be
converted into virtual money, with the help of this money they have to play
sealed bidding for real products. Now, using the remaining money all
the finalists will auction for the Grand Prize.



