				<h1 style="font-variant:small-caps" align="center">About Aaruush</h1>
				<p align="justify">
					SRM University witnesses a furore of activities during the months of July and August. Hordes of students are seen rushing to the Placement Cell immediately after 4:00pm. Some take small breaks en route, to eat an occasional samosa or drink a hot cup of coffee, tea or even Boost for a boost.
					<br>
					<br>
					What could possibly be keeping these students so busy and on their feet, that too in right in the beginning of the academic year? There is one and only one explanation for this. Aaruush.
					<br>
					<br>
					A national level techno-management fest organised by the students of SRM University, Aaruush is a fest that is, by, of and for the students. Sounds clichéd doesn’t it? Well, what can we say? The tale to be told is true. So, here goes nothing…
				</p>
				<br/>
				<div data-role="collapsible-set">
					<div data-role="collapsible" data-collapsed="true">
						<h3>Aaruush '07</h3>
						<p align="justify">
							The year was 2007 and the place, the stairs of a building called placement cell, when 4 final year students geared up with their laptops, to step up and give SRM its very own technical fest. Thus, with the first rays of the rising sun, Aaruush rose. The rest, as they say, is history.
							<br>
							<br>
							Aaruush could not have hoped for a better opening, than one by Dr. APJ Abdul Kalam, the president of India in 2007. 3000 students, 26 events, all to “rise in the spirit of innovation”, echoed, Aaruush was here to stay.
						</p>
					</div>
					<div data-role="collapsible" data-collapsed="true">
						<h3>Aaruush '08</h3>
						<p align="justify">
							The following year saw fully fledged Aaruush offices, students working round the clock to bring out over 40 events along with the DJ nights as the entertainment quotient.
							<br>
							<br>
							This 2nd edition of Aaruush garnered a participation of 4500 students from over 24 states of India, a feat that is not very easy to achieve, just in its 2nd year since conception.
						</p>
					</div>
					<div data-role="collapsible" data-collapsed="true">
						<h3>Aaruush '09</h3>
						<p align="justify">
							It was time to revolutionise the way fests then were held. The third edition witnessed online events, guest lectures by imminent personalities, and workshops for the participants, over 8000 of them from 28 states.
							<br>
							<br>
							Aaruush evolved from a Tech-fest to a Techno-Management fest.
						</p>
					</div>
					<div data-role="collapsible" data-collapsed="true">
						<h3>Aaruush '10</h3>
						<p align="justify">
							“The year of change”, 2010, Aaruush teamed up with some of the largest organizations in the country. In collaboration with DRDO, a Defence Exhibit that included the Arjun Tank left the crowd awestruck.
							<br>
							<br>
							”Conservation of Energy” Serving as the theme, Aaruush stood for several social and environmental causes such as corruption and climate change.
							<br>
							<br>
							Blood donation camps, NGO stalls too garnered a good response from among the 8500 odd participants from almost every state in the country.
						</p>
					</div>
					<div data-role="collapsible" data-collapsed="true">
						<h3>Aaruush '11</h3>
						<p align="justify">
							In its 5th edition, Aaruush  was accredited by the European Southern Observatory, IEEE and the European Space Agency. Striking back with yet another exhibit, DRDO presented, their complete splendour, an Unmanned Aerial as well as an Unmanned Ground Vehicle.
							<br>
							<br>
							Also NGOs like PETA, WWF, HOPE and many others associated themselves with Aaruush. As “Aaruush for Conservation of Energy” continued as the theme, the events and participation toll had doubled since its advent.
							<br>
							<br>
							Aaruush had truly become bigger and better .
						</p>
