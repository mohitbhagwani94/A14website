						<h3> Details </h3>
						<div align="justify">
							<h3>Round 1: BULLSEYE</h3>

							Dante has to reach his planet and requires a target specific rocket. So he needs a smaller
model of his rocket to check if his rocket is accurate and his idea would work.
The participants have to fabricate a single stage water rocket that will be tested
for maximum accuracy. Three concentric circles will be given as a target and
points will be awarded on the basis of distance from the centre of the circle.
Best of two tries will be taken as the final score for this level. Next the
rockets will be tested for maximum range with water level and pressure fixed
for all participants. 


							<h3>Round 2: DOUBLE TROUBLE</h3>
							Dante is convinced that the idea of a rocket would work and needs a proper two stage
rocket that will be a modified version of the existing model. The modified
rocket has to guarantee its fuel efficiency. <br>
This level is focussed on comprehending the abilities and expertise of the participants.
The single stage rocket fabricated in Level 1 will be modified to two stages.<br>
Time will be given for making this modification. Final constructed rocket will
be tested for maximum time of flight when launched vertically and accuracy will
be tested as mentioned in the previous round, using concentric circles.


							<h3>Round 3: TOUCH DOWN</h3>
							Dante is almost ready to use the rocket to reach his planet. He needs the rocket to be
tested for all the flight specifications together one final time with a payload
to confirm that it is ready to take him to his planet. <br>
The final level focuses on innovation and technical expertise. The two stage rocket has
to be made suitable to carry an egg as a payload. The prime judging factor will
be the safe landing of payload. Other factors such as time of flight, and
accuracy will also be considered accordingly.

