						<div align="justify">
							<h3>Round 1: Know it</h3>
							To find the failure of a system, knowing the function of its
components is very important.<br>
This round basically is to test the knowledge of the
participants about the aircraft, its parts and their function. 

							

							<h3>Round 2: Analyse</h3>
							The participants would be shown a video of the crash of an aircraft where the cause
would be evident in it. The participants need to analyse, infer and identify
the cause and provide an in-depth analysis of the catastrophe. They also need
to provide recommendations that could have prevented or can prevent such
accidents.


							<h3>Round 3: Communicate</h3>
							The safety of the aircraft is in the hands of the pilot and
the Air Traffic Controller responsible for the flight in that particular area.<Br>
In this round, one member or the team will be asked to be the pilot and the
other member the controller. A series of disastrous situations will be given to
them and their abilities will be tested. 
The objective is to land the aircraft safely.<br>
We shall be mentioning the type of aircraft, the
manufacturing details and emergency situation guides. 

							<h3>Round 4: Decode the mystery</h3>
							A crash site will be given to the team with the details of
the crash. Dummy parts from the crash will be displayed and ATC transcripts
will be provided. The team who identifies the cause of the crash in the least
amount of time using all the details and parts will be deemed winner. They will
also be asked to make a report giving details of their analysis. This would be
used for validation.
All  the necessary inputs would be provided.


