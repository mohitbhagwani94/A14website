						<div align="justify">
							<h3>Round 1: EXPRESS YOURSELF</h3>

							The participants will be asked to make a poster describing
themselves using Photoshop. With the help of a picture of themselves they need to express themselves with text and graphics or any other feature they wish to
use in photoshop.


							<h3>Round 2: TOWARDS INFINITY</h3>
							The participants will be asked to make an infographic about
aaruush based on the information provided to them. They can manipulate the
design which should be relevant to the theme.


							<h3>Round 3: COMIC ON!</h3>
							Participants are expected to make a comic strip with an original comic character and theme of their choice. It may be funny or related to current affairs but should convey a message. Use of explicit content should be avoided.

							<h3>Round 4: 24 FRAMES</h3>
							Teams will roam around the campus to capture the essence of
aaruush through short videos. They will be provided with some stock videos and will be compiled to make a vine video of a given time length i.e. 2 minutes. 
The video and the designs prepared in the previous rounds
will be compiled in a webpage using Web technology. It will serve as a profile
of the team for judging criteria.

